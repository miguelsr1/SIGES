    /**
     * Crea el objeto indicado con id autogenerada
     *
     * @param entity SgMatricula
     * @return Response SgMatricula
     */
    @POST
    @Operation(summary = "Crea el objeto indicado con id autogenerada.")
    @APIResponses(value = {
        @APIResponse(responseCode = "201", description = "Entidad creada satisfactoriamente.", content = @Content(
                mediaType = MediaType.APPLICATION_JSON,
                schema = @Schema(implementation = SgMatricula.class)))
        ,
        @APIResponse(responseCode = "400", description = "Solicitud con sintaxis incorrecta.")
        ,
        @APIResponse(responseCode = "422", description = "Entidad no se puede procesar. Datos incorrectos.")
        ,
        @APIResponse(responseCode = "500", description = "Error interno del servidor.")}
    )
    @Consumes(MediaType.APPLICATION_JSON)
    @Produces(MediaType.APPLICATION_JSON)
    @RolesAllowed({ConstantesOperaciones.CREAR_MATRICULA})
    //@Timed
    public Response crear(SgMatricula entity) {
        try {
            entity.setMatPk(null);
            entity.setMatVersion(0);
            if (entity.getMatEstudiante().getEstPersona().getPerAllegados() != null) {
                for (SgAllegado fam : entity.getMatEstudiante().getEstPersona().getPerAllegados()) {
                    fam.setAllPersonaReferenciada(entity.getMatEstudiante().getEstPersona());
                }
            }
            if (entity.getMatEstudiante().getEstPersona().getPerTelefonos() != null) {
                for (SgTelefono tel : entity.getMatEstudiante().getEstPersona().getPerTelefonos()) {
                    tel.setTelPersona(entity.getMatEstudiante().getEstPersona());
                }
            }
            if (entity.getMatEstudiante().getEstPersona().getPerIdentificaciones() != null) {
                for (SgIdentificacion ide : entity.getMatEstudiante().getEstPersona().getPerIdentificaciones()) {
                    ide.setIdePersona(entity.getMatEstudiante().getEstPersona());
                }
            }
            if (entity.getMatEstudiante().getEstPersona().getPerResponsable() != null) {
                SgAllegado all = entity.getMatEstudiante().getEstPersona().getPerResponsable();
                if (all.getAllPersona().getPerTelefonos() != null) {
                    for (SgTelefono tel : all.getAllPersona().getPerTelefonos()) {
                        tel.setTelPersona(all.getAllPersona());
                    }
                }
                if (all.getAllPersona().getPerIdentificaciones() != null) {
                    for (SgIdentificacion ide : all.getAllPersona().getPerIdentificaciones()) {
                        ide.setIdePersona(all.getAllPersona());
                    }
                }
            }

            if (entity.getMatEstudiante().getEstPersona().getPerAllegados() != null) {
                for (SgAllegado all : entity.getMatEstudiante().getEstPersona().getPerAllegados()) {
                    if (all.getAllPersona().getPerTelefonos() != null) {
                        for (SgTelefono tel : all.getAllPersona().getPerTelefonos()) {
                            tel.setTelPersona(all.getAllPersona());
                        }
                    }
                    if (all.getAllPersona().getPerIdentificaciones() != null) {
                        for (SgIdentificacion ide : all.getAllPersona().getPerIdentificaciones()) {
                            ide.setIdePersona(all.getAllPersona());
                        }
                    }
                }
            }
            return Response.status(HttpStatus.SC_CREATED).entity(servicio.guardar(entity)).build();
        } catch (BusinessException be) {
            return Response.status(HttpStatus.SC_UNPROCESSABLE_ENTITY).entity(new BusinessReturnedException(be.getErrores())).build();
        } catch (Exception ex) {
            if (PersistenceHelper.isOptimisticException(ex)) {
                return Response.status(HttpStatus.SC_UNPROCESSABLE_ENTITY).entity(new BusinessReturnedException(Errores.ERROR_OPTIMISTIC_LOCK_MATRICULA)).build();
            } else if (PersistenceHelper.isConstraintViolation(ex)) {
                return Response.status(HttpStatus.SC_UNPROCESSABLE_ENTITY).entity(PersistenceHelper.getMatriculaViolationBusinessException(ex)).build();
            } else {
                tracer.activeSpan().setTag(Constantes.OPENTRACING_ERROR_TAG, true);
                tracer.activeSpan().log(ex.getMessage());
                LOGGER.log(Level.SEVERE, jwt.getSubject() + " | " + tracer.activeSpan().context().toString(), ex);
                return Response.status(HttpStatus.SC_INTERNAL_SERVER_ERROR).entity(Errores.ERROR_GENERAL).build();
            }
        }
    }

