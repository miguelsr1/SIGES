    @GET
    @Path("/procesararchivo")
    @PermitAll
    public Response procesarArchivo() {
        try {
            FiltroCodiguera fc = new FiltroCodiguera();
            fc.setCodigoExacto(Constantes.IMPORTAR_BIT_MATRICULA_ASINC);
            List<SgConfiguracion> configList = configuracionBean.obtenerPorFiltro(fc);
            if (!configList.isEmpty()) {
                if (configList.get(0).getConValor().equals("1")) {
                    FiltroArchivoBitMatricula fac = new FiltroArchivoBitMatricula();
                    fac.setAbmEstado(EnumEstadoImportado.NO_PROCESADO);
                    List<SgArchivoBitMatricula> arCalList = servicio.obtenerPorFiltro(fac);
                    for (SgArchivoBitMatricula arCal : arCalList) {
                        try {
                            matriculaBean.procesarArchivos(arCal);

                        } catch (Exception ex) {
                            Logger.getLogger(ArchivoBitMatriculaBean.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                    return Response.status(HttpStatus.SC_OK).build();
                }
            }

            BusinessException be = new BusinessException();
            be.addError(Errores.ERROR_CONFIGURACION_IMPORTAR_CALIFICACION);
            return Response.status(HttpStatus.SC_UNPROCESSABLE_ENTITY).entity(new BusinessReturnedException(be.getErrores())).build();
        } catch (Exception ex) {
            LOGGER.log(Level.SEVERE, ex.getMessage(), ex);
            return Response.status(HttpStatus.SC_INTERNAL_SERVER_ERROR).entity(Errores.ERROR_GENERAL).build();
        }
    }

